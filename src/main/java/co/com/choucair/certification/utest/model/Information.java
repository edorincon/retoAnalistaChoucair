package co.com.choucair.certification.utest.model;

public class Information {

    private String strName;
    private String strLastName;
    private String strEmail;
    private String strPassword;
    private String strConfirmPassword;
    private String strVerify;

    public String getStrName() {
        return strName;
    }

    public void setStrName(String strName) {
        this.strName = strName;
    }

    public String getStrLastName() {
        return strLastName;
    }

    public void setStrLastName(String strLastName) {
        this.strLastName = strLastName;
    }

    public String getStrEmail() {
        return strEmail;
    }

    public void setStrEmail(String strEmail) {
        this.strEmail = strEmail;
    }

    public String getStrPassword() {
        return strPassword;
    }

    public void setStrPassword(String strPassword) {
        this.strPassword = strPassword;
    }

    public String getStrConfirmPassword() {
        return strConfirmPassword;
    }

    public void setStrConfirmPassword(String strConfirmPassword) {
        this.strConfirmPassword = strConfirmPassword;
    }

    public String getStrVerify() {
        return strVerify;
    }

    public void setStrVerify(String strVerify) {
        this.strVerify = strVerify;
    }
}
